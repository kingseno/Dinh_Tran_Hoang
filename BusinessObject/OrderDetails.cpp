/*******************************************************************************
** Function Name         : OrderDetails
**
** Description           : This is constructor function to create a new object
**
** Author                : Dinh Pham
*******************************************************************************/
OrderDetails::OrderDetails(int orderDetailId, int orderId, int productId,
 int quantity) {
    m_OrderDetailId = orderDetailId;
    m_OrderId = orderId;
    m_ProductId = productId;
    m_Quantity = quantity;
}

/*******************************************************************************
** Function Name         : toString
**
** Description           : Convert a product object into a string
**
** Author                : Dinh Pham
*******************************************************************************/
string OrderDetails::toString(){
    string s;
    s += "{";
    s += to_string(m_OrderDetailId) + ", ";
    s += to_string(m_OrderId) + ", ";
    s += to_string(m_ProductId) + ", ";
    s += to_string(m_Quantity);
    s += "}";
    return s;
}

/*******************************************************************************
** Function Name         : toJson
**
** Description           : Convert a product object into a json object
**
** Author                : Dinh Pham
*******************************************************************************/
json OrderDetails::toJson(){
    json j;
    j["orderDetailId"] = m_OrderDetailId;
    j["orderId"] = m_OrderId;
    j["productId"] = m_ProductId;
    j["quantity"] = m_Quantity;
    return j;
}

