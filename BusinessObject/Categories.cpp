/*******************************************************************************
** Function Name         : Categories
**
** Description           : This is constructor function to create a new object
**
** Author                : Dinh Pham
*******************************************************************************/
Categories::Categories(int categoryId, string categoryName,
 string description) {
    m_CategoryId = categoryId;
    m_CategoryName = categoryName;
    m_Description = description;
}

/*******************************************************************************
** Function Name         : toString
**
** Description           : Convert a product object into a string
**
** Author                : Dinh Pham
*******************************************************************************/
string Categories::toString(){
    string s;
    s += "{";
    s += to_string(m_CategoryId) + ", ";
    s += m_CategoryName + ", ";
    s += m_Description;
    s += "}";
    return s;
}

/*******************************************************************************
** Function Name         : toJson
**
** Description           : Convert a product object into a json object
**
** Author                : Dinh Pham
*******************************************************************************/
json Categories::toJson(){
    json j;
    j["categoryId"] = m_CategoryId;
    j["categoryName"] = m_CategoryName;
    j["description"] = m_Description;
    return j;
}

